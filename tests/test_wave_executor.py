class TestParseScheduleMd:
    def test_feature_name(self, plan: ExecutionPlan):
        assert plan.feature_name == "Test Feature"

    def test_makespan(self, plan: ExecutionPlan):
        assert plan.makespan == 10

    def test_wave_count(self, plan: ExecutionPlan):
        assert len(plan.waves) == 2

    def test_wave_indices(self, plan: ExecutionPlan):
        assert plan.waves[0].index == 1
        assert plan.waves[1].index == 2

    def test_wave_start_times(self, plan: ExecutionPlan):
        assert plan.waves[0].start_time == 0
        assert plan.waves[1].start_time == 5

    def test_task_in_wave1(self, plan: ExecutionPlan):
        t = plan.waves[0].tasks[0]
        assert t.task_id == "T001"
        assert t.agent_id == "backend"
        assert t.duration == 5
        assert t.start == 0
        assert t.end == 5
        assert t.files == ("src/a.py",)
        assert t.phase == "Setup"

    def test_task_in_wave2(self, plan: ExecutionPlan):
        t = plan.waves[1].tasks[0]
        assert t.task_id == "T002"
        assert t.agent_id == "tester"
        assert t.start == 5
        assert t.end == 10

    def test_agents_parsed(self, plan: ExecutionPlan):
        assert len(plan.agents) == 2
        ids = {a.id for a in plan.agents}
        assert ids == {"backend", "tester"}

    def test_agent_model(self, plan: ExecutionPlan):
        backend = next(a for a in plan.agents if a.id == "backend")
        assert backend.model == "claude-sonnet"

    def test_agent_no_provider(self, plan: ExecutionPlan):
        backend = next(a for a in plan.agents if a.id == "backend")
        assert backend.provider is None

    def test_returns_frozen_plan(self, plan: ExecutionPlan):
        with pytest.raises((AttributeError, TypeError)):
            plan.feature_name = "mutated"  # type: ignore[misc]

    def test_accepts_path_object(self, minimal_md: Path):
        p = parse_schedule_md(minimal_md)
        assert isinstance(p, ExecutionPlan)

    def test_accepts_string_path(self, minimal_md: Path):
        p = parse_schedule_md(str(minimal_md))
        assert isinstance(p, ExecutionPlan)


