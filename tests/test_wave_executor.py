@pytest.mark.skipif(
    not EXAMPLE_SCHEDULE.exists(),
    reason="docs/example-schedule.md not present",
)
class TestExampleRoundTrip:
    def test_parses_without_error(self):
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        assert isinstance(plan, ExecutionPlan)

    def test_feature_name(self):
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        assert plan.feature_name == "Create Taskify"

    def test_makespan(self):
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        assert plan.makespan == 315

    def test_wave_count(self):
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        assert len(plan.waves) > 0

    def test_agent_count(self):
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        assert len(plan.agents) == 5

    def test_all_tasks_present(self):
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        all_task_ids = {t.task_id for w in plan.waves for t in w.tasks}
        # example has T001-T028
        assert len(all_task_ids) == 28

    def test_json_roundtrip(self):
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        d = json.loads(_emit_json(plan))
        assert d["makespan"] == plan.makespan
        assert len(d["waves"]) == len(plan.waves)

    def test_shell_syntax(self, tmp_path: Path):
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        script = tmp_path / "plan.sh"
        script.write_text(_emit_shell(plan), encoding="utf-8")
        result = subprocess.run(
            ["bash", "-n", str(script)], capture_output=True
        )
        assert result.returncode == 0, result.stderr.decode()

    def test_table_has_all_tasks(self):
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        table = _emit_table(plan)
        for wave in plan.waves:
            for task in wave.tasks:
                assert task.task_id in table

    def test_cli_json_output(self, capsys):
        rc = main([str(EXAMPLE_SCHEDULE), "--format", "json"])
        assert rc == 0
        out = capsys.readouterr().out
        d = json.loads(out)
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        assert d["makespan"] == 315
        assert len(d["waves"]) == len(plan.waves)

    def test_task_start_end_consistency(self):
        """start + duration == end for every parsed task."""
        plan = parse_schedule_md(EXAMPLE_SCHEDULE)
        for wave in plan.waves:
            for task in wave.tasks:
                assert task.start + task.duration == task.end, (
                    f"{task.task_id}: {task.start} + {task.duration} != {task.end}"
                )
