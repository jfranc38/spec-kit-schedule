# Plotly figure dimensions (Gantt chart). Centralised so the renderer
# can be tuned without hunting through the chart-builder body.
_GANTT_MIN_HEIGHT = 300
_GANTT_PER_AGENT_HEIGHT = 80
_GANTT_BAR_WIDTH = 0.55
_GANTT_MARGIN_T = 60
_GANTT_MARGIN_R = 20
_GANTT_MARGIN_B = 60
_GANTT_MARGIN_L = 120
# Extra title-area padding included in the height calculation so the
# top margin doesn't crowd a one-row Gantt.
_GANTT_TITLE_PAD = 120

_FALLBACK_COLOR = "#888888"
_RESOURCE_EDGE_COLOR = "#999999"
_PARSER_EDGE_COLOR = "#AAAAAA"
_CRITICAL_EDGE_COLOR = CRITICAL_COLOR
_CRITICAL_MARKER_PATTERN = "/"
_CRITICAL_BORDER_COLOR = CRITICAL_COLOR


def _require_plotly() -> None:
    try:
        import plotly  # noqa: F401
    except ImportError as exc:
        raise ImportError(
            "plotly is required for solver.render_html. Install with "
            "`uv sync --extra viz` or `pip install 'plotly>=5,<6'`."
        ) from exc


def _esc(text: object) -> str:
    return html.escape(str(text))


def _color_for_agent(agent_id: str, agents_sorted: list[str]) -> str:
    try:
        idx = agents_sorted.index(agent_id)
        return AGENT_COLORS[idx % len(AGENT_COLORS)]
    except ValueError:
        return _FALLBACK_COLOR


