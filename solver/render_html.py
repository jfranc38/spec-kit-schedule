def _header_html(data: dict[str, Any], feature_name: str) -> str:
    stats = data.get("stats", {})
    status = stats.get("status", data.get("status", STATUS_UNKNOWN))
    makespan = stats.get("makespan", "?")
    waves = stats.get("total_waves", "?")
    agents = stats.get("total_agents", "?")
    ts = datetime.now(tz=timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    badge_color = "#2ecc71" if STATUS_OPTIMAL in str(status) else (
        "#f39c12" if STATUS_FEASIBLE in str(status) else "#e74c3c"
    )
    return f"""
<div class="card header-card">
  <h1>Schedule &mdash; {_esc(feature_name)}</h1>
  <p>Generated by <code>/speckit.schedule</code> &mdash; CP-SAT Multi-Agent Optimizer
  &mdash; {_esc(ts)}</p>
  <div class="badges">
    <span class="badge" style="background:{_esc(badge_color)}">
      Status: {_esc(status)}
    </span>
    <span class="badge">Makespan: <strong>{_esc(makespan)}</strong></span>
    <span class="badge">Waves: <strong>{_esc(waves)}</strong></span>
    <span class="badge">Agents: <strong>{_esc(agents)}</strong></span>
  </div>
</div>
"""


def _warnings_html(data: dict[str, Any]) -> str:
    warnings = data.get("warnings", [])
    if not warnings:
        return ""
    items = []
    for w in warnings:
        code = w.get("code", "warning")
        msg = w.get("message", "")
        ctx = w.get("context") or {}
        ctx_str = ", ".join(f"{k}={v!r}" for k, v in ctx.items())
        ctx_html = f" <small>({_esc(ctx_str)})</small>" if ctx_str else ""
        items.append(
            f"<li><strong>[{_esc(code)}]</strong> {_esc(msg)}{ctx_html}</li>"
        )
    return f"""
<div class="card warnings-card">
  <h2>&#9888; Warnings</h2>
  <ul>{"".join(items)}</ul>
</div>
"""


def _agent_assignments_html(data: dict[str, Any], agents_sorted: list[str]) -> str:
    assignments = data.get("assignments", [])
    agent_summary = data.get("agent_summary", [])
    by_agent: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for a in assignments:
        by_agent[a["agent_id"]].append(a)

    summary_by_id = {s["agent_id"]: s for s in agent_summary}
    cards = []
    for agent_id in agents_sorted:
        color = _color_for_agent(agent_id, agents_sorted)
        summary = summary_by_id.get(agent_id, {})
        budget_pct = summary.get("budget_utilization", 0)
        kappa_pct = summary.get("kappa_utilization", 0)
        model_label = format_agent_model_label(summary, sep="&middot;", esc=_esc)
        task_count = summary.get("task_count", 0)
        total_tokens = summary.get("total_tokens", 0)

        task_items = []
        for t in sorted(by_agent.get(agent_id, []), key=lambda x: x["start"]):
            files = ", ".join(f"<code>{_esc(f)}</code>" for f in t.get("file_paths", []))
            label = task_label(t)
            task_items.append(
                f"<li><strong>{_esc(t['task_id'])}</strong> "
                f"[{_esc(label)}] "
                f"t={_esc(t['start'])}&rarr;{_esc(t['end'])} | {files}</li>"
            )

        cards.append(f"""
<div class="agent-card" style="border-left: 4px solid {_esc(color)}">
  <h3 style="color:{_esc(color)}">{_esc(agent_id)}</h3>
  <p>{model_label} &mdash; {_esc(task_count)} tasks,
  {_esc(f"{total_tokens:,}")} tokens
  ({_esc(budget_pct)}% budget, {_esc(kappa_pct)}% &kappa;)</p>
  <ul>{"".join(task_items)}</ul>
</div>""")

    return f"""
<div class="card">
  <h2>Agent Assignments</h2>
  <div class="agent-grid">{"".join(cards)}</div>
</div>
"""


def _waves_html(data: dict[str, Any]) -> str:
    waves = data.get("waves", [])
    if not waves:
        return ""
    items = []
    for wave in waves:
        wave_tasks = wave.get("tasks", [])
        wave_num = wave.get("wave", "?")
        start_time = wave.get("start_time", "?")
        phases = " + ".join(sorted({t.get("phase", "?") for t in wave_tasks}))
        open_attr = ' open' if wave_num == 1 else ""
        rows = []
        for t in wave_tasks:
            files = ", ".join(f"<code>{_esc(f)}</code>" for f in t.get("file_paths", []))
            label = t.get("story_id") or t.get("phase", "?")
            rows.append(
                f"<tr><td>{_esc(t['task_id'])}</td>"
                f"<td>{_esc(t.get('agent_id','?'))}</td>"
                f"<td>{_esc(t.get('duration','?'))}</td>"
                f"<td>{files}</td>"
                f"<td>{_esc(label)}</td></tr>"
            )
        items.append(f"""
<details{open_attr}>
  <summary>Wave {_esc(wave_num)} (t={_esc(start_time)}) &mdash; {_esc(phases)}</summary>
  <table>
    <thead><tr>
      <th>Task</th><th>Agent</th><th>Duration</th><th>Files</th><th>Phase</th>
    </tr></thead>
    <tbody>{"".join(rows)}</tbody>
  </table>
</details>""")

    return f"""
<div class="card">
  <h2>Execution Waves</h2>
  {"".join(items)}
</div>
"""


def _critical_path_html(data: dict[str, Any]) -> str:
    critical_path: list[str] = data.get("critical_path", [])
    if not critical_path:
        return ""
    assignments = data.get("assignments", [])
    by_id = {a["task_id"]: a for a in assignments}
    rows = []
    cumulative = 0
    for i, task_id in enumerate(critical_path, 1):
        a = by_id.get(task_id)
        if a is None:
            continue
        cumulative += a["duration"]
        rows.append(
            f"<tr><td>{i}</td>"
            f"<td><strong>{_esc(task_id)}</strong></td>"
            f"<td>{_esc(a['agent_id'])}</td>"
            f"<td>{_esc(a['start'])}</td>"
            f"<td>{_esc(a['end'])}</td>"
            f"<td>{_esc(a['duration'])}</td>"
            f"<td>{cumulative}</td></tr>"
        )
    return f"""
<div class="card">
  <h2>Critical Path</h2>
  <p>Tasks on this chain dictate the makespan. Reducing their duration
  is the only way to shorten the project.</p>
  <table>
    <thead><tr>
      <th>#</th><th>Task</th><th>Agent</th>
      <th>Start</th><th>End</th><th>Duration</th><th>Cumulative</th>
    </tr></thead>
    <tbody>{"".join(rows)}</tbody>
  </table>
</div>
"""


def _stats_html(data: dict[str, Any]) -> str:
    stats = data.get("stats", {})
    max_load = stats.get("max_load")
    min_load = stats.get("min_load")
    load_range = (
        max_load - min_load
        if isinstance(max_load, int) and isinstance(min_load, int)
        else "?"
    )
    rows_data = [
        ("Total tasks", stats.get("total_tasks", "?")),
        ("Total agents", stats.get("total_agents", "?")),
        ("Makespan (C_max)", f"{stats.get('makespan', '?')} time units"),
        ("Max agent load (L_max)", f"{max_load if max_load is not None else '?'} time units"),
        ("Min agent load (L_min)", f"{min_load if min_load is not None else '?'} time units"),
        ("Load range", f"{load_range} time units"),
        ("Execution waves", stats.get("total_waves", "?")),
        ("Horizon", stats.get("horizon", "?")),
        ("Phase 1 time", f"{stats.get('phase1_time', '?')}s"),
        ("Phase 1 status", stats.get("phase1_status", "?")),
        ("Phase 2 time", f"{stats.get('phase2_time', '?')}s"),
        ("Phase 2 status", stats.get("phase2_status", "?")),
        ("Status", stats.get("status", "?")),
    ]
    rows = "".join(
        f"<tr><td>{_esc(k)}</td><td>{_esc(v)}</td></tr>"
        for k, v in rows_data
    )
    return f"""
<div class="card">
  <h2>Solver Statistics</h2>
  <table>
    <thead><tr><th>Metric</th><th>Value</th></tr></thead>
    <tbody>{rows}</tbody>
  </table>
</div>
"""


