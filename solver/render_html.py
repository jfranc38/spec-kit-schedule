def _build_gantt_figure(data: dict[str, Any], agents_sorted: list[str]) -> object:
    """Return a plotly Figure for the Gantt chart."""
    _require_plotly()
    import plotly.graph_objects as go

    assignments = data.get("assignments", [])
    critical_set = set(data.get("critical_path", []))
    agent_summary = data.get("agent_summary", [])

    # Look up price from original agents list (if solver carried it through)
    agents_raw = data.get("agents", [])
    price_map: dict[str, float] = {
        a["id"]: float(a.get("price_per_1k_tokens", 0.0))
        for a in agents_raw
        if isinstance(a, dict)
    }
    price_by_agent: dict[str, float] = {
        s["agent_id"]: price_map.get(s["agent_id"], 0.0)
        for s in agent_summary
    }

    row_map = {ag: i for i, ag in enumerate(agents_sorted)}
    traces = []
    for a in assignments:
        agent_id = a["agent_id"]
        is_crit = a["task_id"] in critical_set
        color = _color_for_agent(agent_id, agents_sorted)
        tokens = a.get("tokens", 0)
        price = price_by_agent.get(agent_id, 0.0)
        cost_str = f"${cost_dollars(tokens, price):.4f}" if price > 0 else "n/a"
        files_str = ", ".join(a.get("file_paths", []))
        hover = (
            f"<b>{a['task_id']}</b><br>"
            f"Agent: {agent_id}<br>"
            f"Duration: {a['duration']}<br>"
            f"t: {a['start']} &rarr; {a['end']}<br>"
            f"Tokens: {tokens:,}<br>"
            f"Files: {files_str or 'none'}<br>"
            f"Critical: {'yes' if is_crit else 'no'}<br>"
            f"Cost: {cost_str}"
        )
        marker: dict[str, Any] = {
            "color": color,
            "line": {
                "color": _CRITICAL_BORDER_COLOR if is_crit else "#333333",
                "width": 3 if is_crit else 0.5,
            },
        }
        if is_crit:
            marker["pattern"] = {"shape": _CRITICAL_MARKER_PATTERN, "fgcolor": "#ffffff"}

        traces.append(go.Bar(
            x=[a["end"] - a["start"]],
            y=[row_map.get(agent_id, 0)],
            base=[a["start"]],
            orientation="h",
            name=a["task_id"],
            text=[a["task_id"]],
            textposition="inside",
            insidetextanchor="middle",
            hovertemplate=hover + "<extra></extra>",
            marker=marker,
            showlegend=False,
            width=_GANTT_BAR_WIDTH,
        ))

    fig = go.Figure(data=traces)
    fig.update_layout(
        title="Interactive Gantt Chart",
        xaxis_title="Time units",
        yaxis={
            "tickvals": list(range(len(agents_sorted))),
            "ticktext": agents_sorted,
            "autorange": "reversed",
        },
        barmode="overlay",
        height=max(
            _GANTT_MIN_HEIGHT,
            len(agents_sorted) * _GANTT_PER_AGENT_HEIGHT + _GANTT_TITLE_PAD,
        ),
        template="plotly_white",
        margin={
            "l": _GANTT_MARGIN_L,
            "r": _GANTT_MARGIN_R,
            "t": _GANTT_MARGIN_T,
            "b": _GANTT_MARGIN_B,
        },
        legend_title="Legend",
    )
    # Legend items for agents
    for _i, ag in enumerate(agents_sorted):
        color = _color_for_agent(ag, agents_sorted)
        fig.add_trace(go.Bar(
            x=[0], y=[0], base=[0],
            orientation="h",
            name=ag,
            marker={"color": color},
            showlegend=True,
            visible="legendonly",
            hoverinfo="skip",
        ))
    return fig


def _hierarchical_positions(
    nodes: list[str],
    edges_all: list[tuple[str, str]],
) -> dict[str, tuple[float, float]]:
    """Assign (x, y) positions using topological depth (x) and spread (y)."""
    adj: dict[str, list[str]] = {n: [] for n in nodes}
    in_deg: dict[str, int] = dict.fromkeys(nodes, 0)
    for src, dst in edges_all:
        if src in adj and dst in in_deg:
            adj[src].append(dst)
            in_deg[dst] += 1

    # Kahn topological sort + layer assignment (longest path to node)
    depth: dict[str, int] = dict.fromkeys(nodes, 0)
    queue = sorted(n for n, d in in_deg.items() if d == 0)
    topo: list[str] = []
    temp_deg = dict(in_deg)
    while queue:
        node = queue.pop(0)
        topo.append(node)
        for nxt in sorted(adj[node]):
            depth[nxt] = max(depth[nxt], depth[node] + 1)
            temp_deg[nxt] -= 1
            if temp_deg[nxt] == 0:
                queue.append(nxt)

    # Group by depth, spread vertically
    by_depth: dict[int, list[str]] = defaultdict(list)
    for n in topo:
        by_depth[depth[n]].append(n)
    for remaining in nodes:
        if remaining not in depth:
            by_depth[0].append(remaining)

    pos: dict[str, tuple[float, float]] = {}
    for x_val, col_nodes in sorted(by_depth.items()):
        for y_idx, node in enumerate(sorted(col_nodes)):
            pos[node] = (float(x_val), float(y_idx))
    return pos


def _build_dag_figure(data: dict[str, Any], agents_sorted: list[str]) -> object:
    """Return a plotly Figure for the dependency DAG."""
    _require_plotly()
    import plotly.graph_objects as go

    assignments = data.get("assignments", [])
    parser_edges = [(e[0], e[1]) for e in data.get("edges", [])]
    resource_edges = [(e[0], e[1]) for e in data.get("resource_edges", [])]
    critical_path = data.get("critical_path", [])
    critical_set = set(critical_path)
    cp_edges_raw = data.get("critical_path_edges", list(pairwise(critical_path)))
    critical_edges = [(e[0], e[1]) for e in cp_edges_raw]

    nodes = [a["task_id"] for a in assignments]
    agent_of: dict[str, str] = {a["task_id"]: a["agent_id"] for a in assignments}

    all_edges_for_layout: list[tuple[str, str]] = list({
        *parser_edges, *resource_edges, *critical_edges,
    })
    pos = _hierarchical_positions(nodes, all_edges_for_layout)

    # Scale positions for better spacing
    max_x = max((xy[0] for xy in pos.values()), default=1)
    max_y = max((xy[1] for xy in pos.values()), default=1)
    x_scale = 2.0 if max_x == 0 else 10.0 / max(max_x, 1)
    y_scale = 2.0 if max_y == 0 else 6.0 / max(max_y, 1)

    scaled_pos: dict[str, tuple[float, float]] = {
        n: (xy[0] * x_scale, xy[1] * y_scale) for n, xy in pos.items()
    }

    def _make_edge_trace(
        edges: list[tuple[str, str]],
        color: str,
        dash: str,
        width: float,
        name: str,
    ) -> object:
        ex: list[float | None] = []
        ey: list[float | None] = []
        for src, dst in edges:
            if src not in scaled_pos or dst not in scaled_pos:
                continue
            sx, sy = scaled_pos[src]
            dx, dy = scaled_pos[dst]
            ex += [sx, dx, None]
            ey += [sy, dy, None]
        return go.Scatter(
            x=ex, y=ey,
            mode="lines",
            line={"color": color, "dash": dash, "width": width},
            name=name,
            hoverinfo="skip",
        )

    traces: list[object] = []
    drawn_crit = set(critical_edges)
    non_crit_parser = [e for e in parser_edges if e not in drawn_crit]
    non_crit_resource = [e for e in resource_edges if e not in drawn_crit]

    if non_crit_parser:
        traces.append(_make_edge_trace(
            non_crit_parser, _PARSER_EDGE_COLOR, "solid", 1.0, "Parser edges",
        ))
    if non_crit_resource:
        traces.append(_make_edge_trace(
            non_crit_resource, _RESOURCE_EDGE_COLOR, "dash", 1.0, "Resource edges",
        ))
    if critical_edges:
        traces.append(_make_edge_trace(
            critical_edges, _CRITICAL_EDGE_COLOR, "solid", 3.0, "Critical path",
        ))

    # Node trace (one per agent colour for legend)
    agent_to_nodes: dict[str, list[str]] = defaultdict(list)
    for node in nodes:
        agent_to_nodes[agent_of.get(node, "")].append(node)

    for agent_id in agents_sorted:
        color = _color_for_agent(agent_id, agents_sorted)
        agent_nodes = agent_to_nodes.get(agent_id, [])
        if not agent_nodes:
            continue
        nx_coords = [scaled_pos[n][0] for n in agent_nodes if n in scaled_pos]
        ny_coords = [scaled_pos[n][1] for n in agent_nodes if n in scaled_pos]
        is_crit = [n in critical_set for n in agent_nodes if n in scaled_pos]
        valid_nodes = [n for n in agent_nodes if n in scaled_pos]
        sizes = [18 if c else 12 for c in is_crit]
        borders = [_CRITICAL_BORDER_COLOR if c else "#333333" for c in is_crit]
        border_widths = [3 if c else 1 for c in is_crit]
        hovers = [
            f"<b>{n}</b><br>Agent: {agent_id}<br>Critical: {'yes' if c else 'no'}"
            for n, c in zip(valid_nodes, is_crit, strict=True)
        ]
        traces.append(go.Scatter(
            x=nx_coords, y=ny_coords,
            mode="markers+text",
            name=agent_id,
            text=valid_nodes,
            textposition="top center",
            textfont={"size": 9},
            hovertemplate=[h + "<extra></extra>" for h in hovers],
            marker={
                "color": color,
                "size": sizes,
                "line": {"color": borders, "width": border_widths},
            },
        ))

    height = max(400, len(nodes) * 8 + 200)
    fig = go.Figure(data=traces)
    fig.update_layout(
        title="Interactive Dependency DAG",
        xaxis={"visible": False},
        yaxis={"visible": False},
        height=height,
        template="plotly_white",
        margin={"l": 20, "r": 20, "t": 60, "b": 20},
        legend_title="Legend",
    )
    return fig


