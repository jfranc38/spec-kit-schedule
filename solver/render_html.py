def _build_argparser() -> argparse.ArgumentParser:
    ap = argparse.ArgumentParser(
        prog="render_html.py",
        description="Render solver output JSON into a self-contained schedule.html.",
    )
    ap.add_argument("input", help="Path to solver output JSON")
    ap.add_argument("feature_name")
    ap.add_argument(
        "--inline-plotly",
        action="store_true",
        default=False,
        help="Embed full plotly.js bundle (~4 MB) for fully offline output.",
    )
    return ap


def main(argv: list[str] | None = None) -> int:
    args = _build_argparser().parse_args(argv)
    with open(args.input, encoding="utf-8") as f:
        data = json.load(f)
    try:
        html_out = render(
            data,
            args.feature_name,
            inline_plotly=args.inline_plotly,
        )
    except ImportError as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 2
    sys.stdout.write(html_out)
    sys.stdout.write("\n")
    return 0


if __name__ == "__main__":
    sys.exit(main())
